def build_prompt_records():
    records = []
    generic_templates = [
        "a travel destination photo in {destination} Vietnam",
        "a tourism photo representing {destination} in Vietnam",
        "a smartphone travel photo taken in {destination} Vietnam",
        "a beautiful tourist attraction in {destination} Vietnam",
        "ảnh du lịch tại {destination} Việt Nam",
        "ảnh check in tại {destination} Việt Nam",
    ]

    scene_templates = {
        "beach": [
            "a sunny beach travel photo in {destination} Vietnam",
            "a tropical sea and sand travel photo in {destination} Vietnam",
        ],
        "coastal": [
            "a blue coast and ocean travel photo in {destination} Vietnam",
        ],
        "mountain": [
            "a mountain landscape travel photo in {destination} Vietnam",
            "a cloud hunting travel photo in {destination} Vietnam",
        ],
        "city": [
            "a city landmark travel photo in {destination} Vietnam",
        ],
        "culture": [
            "a cultural heritage travel photo in {destination} Vietnam",
        ],
        "resort": [
            "a beach resort travel photo in {destination} Vietnam",
        ],
        "cruise": [
            "a cruise travel photo in {destination} Vietnam",
        ],
        "forest": [
            "a pine forest travel photo in {destination} Vietnam",
        ],
    }

    for item in DESTINATION_CATALOG:
        for template in generic_templates:
            records.append(
                {
                    "destination": item["destination"],
                    "label": f"Toàn cảnh {item['destination']}",
                    "prompt": template.format(destination=item["destination"]),
                }
            )
        for scene_tag in item.get("scene_tags", []):
            for template in scene_templates.get(scene_tag, []):
                records.append(
                    {
                        "destination": item["destination"],
                        "label": f"Scene {scene_tag} {item['destination']}",
                        "prompt": template.format(destination=item["destination"]),
                    }
                )
        for alias in item.get("aliases", [])[:5]:
            records.append(
                {
                    "destination": item["destination"],
                    "label": f"Alias {item['destination']}",
                    "prompt": f"a travel photo related to {alias} in Vietnam",
                }
            )
        for landmark in item["landmarks"]:
            records.append(
                {
                    "destination": item["destination"],
                    "label": landmark["label"],
                    "prompt": landmark["prompt"],
                }
            )
            records.append(
                {
                    "destination": item["destination"],
                    "label": landmark["label"],
                    "prompt": f"ảnh du lịch {landmark['label']} tại {item['destination']} Việt Nam",
                }
            )
    return records
